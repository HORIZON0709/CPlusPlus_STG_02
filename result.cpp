//================================================
//更新
//================================================
void CResult::Update()
{
	CObject::UpdateAll();	//オブジェクト

	CInput* pInput = CInput::GetKey();	//キーボード情報を取得

	if (pInput->Trigger(CInput::STANDARD_KEY::DECISION))
	{//決定キ―
		SetMode(MODE::TITLE);	//モードの設定
	}
}

