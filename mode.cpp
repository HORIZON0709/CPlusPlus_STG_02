//================================================
//初期化
//================================================
HRESULT CMode::Init()
{
	/* ゲーム */

	if (m_pGame == nullptr)
	{//NULLチェック
		m_pGame = new CGame;	//メモリの動的確保
	}

	if (FAILED(m_pGame->Init()))
	{//初期化処理が失敗した場合
		return E_FAIL;
	}

	return S_OK;
}

//================================================
//終了
//================================================
void CMode::Uninit()
{
	/* ゲーム */

	if (m_pGame != nullptr)
	{//NULLチェック
		m_pGame->Uninit();	//終了
		delete m_pGame;		//メモリの解放
		m_pGame = nullptr;	//nullptrにする
	}
}

